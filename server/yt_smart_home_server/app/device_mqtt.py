def device_mqtt_config(device_no: str, heartbeat_interval_ms: int) -> dict[str, Any]:
    """Return the MQTT connection config sent to a device inside YTS-SEC/1.

    MVP uses a fixed broker password provisioned on the broker and stored only in
    the server environment. It is intentionally separate from the device AES key.
    Production should replace this with short-lived per-device tokens.
    """
    settings = get_settings()
    enabled = bool(settings.mqtt_enabled and settings.mqtt_device_password)
    username = device_no
    return {
        "enabled": enabled,
        "version": 1,
        "credentialMode": "fixed_broker_password_mvp",
        "host": settings.mqtt_host,
        "port": settings.mqtt_port,
        "protocol": "mqtts",
        "mqttVersion": "3.1.1",
        "clientId": f"yt_{device_no}",
        "username": username,
        "password": settings.mqtt_device_password if enabled else "",
        "expiresAt": None,
        "cleanSession": True,
        "keepAliveSeconds": settings.mqtt_keepalive_seconds,
        "heartbeatIntervalMs": heartbeat_interval_ms,
        "qos": {"down": 1, "ack": 1, "status": 1, "telemetry": 0},
        "topics": {
            "up": f"yt/v1/devices/{device_no}/up",
            "down": f"yt/v1/devices/{device_no}/down",
            "status": f"yt/v1/devices/{device_no}/status",
        },
        "tls": {
            "enabled": settings.mqtt_tls,
            "verifyRequired": True,
            "serverName": settings.mqtt_host,
            "caName": "ISRG Root X1",
            "caPem": ISRG_ROOT_X1_PEM,
        },
    }
